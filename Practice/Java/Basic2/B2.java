package Practice.Java.Basic2;

public class B2 {
    public static void main(String[] args){
        int num = 0;
        //numの2で割った時の商が0=偶数
        if (num%2==0){
            System.out.println(num+"は偶数です。");
        //違う場合は奇数
        }else{
            System.out.println(num+"は奇数です。");
        }
    }
}
