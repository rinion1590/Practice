package Practice.Java.Basic1;

public class B1 {
    public static void main(String[] args){
        //変数 HelloWorldを代入
        String Hello = "HelloWorld";
        //変数HelloとHiをくっつけてprint
        System.out.println("Hi "+Hello);
        //変数numに3を代入
        int num =  3;
        System.out.println(num);
        //numをインクリメント
        num++;
        System.out.println(num);
        //numにnum*2を代入
        num*=2;
        System.out.println(num);
}
}
